    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if (camposVacios()) {
            JOptionPane.showMessageDialog(null, "Llene todos los campos");
        }else if (Integer.parseInt(txtIdFactura.getText()) < fbo.getMaxID()) {
            JOptionPane.showMessageDialog(null, "El registro con codigo "+txtIdFactura.getText()+" ya existe");
        }else{
            Fac_Detalle fac = new Fac_Detalle();
            fac.setIdFac_Detalle(Integer.parseInt(txtIdFactura.getText()));
            fac.setCantidad(Integer.parseInt(txtCantidad.getText()));
            fac.setTotal(Double.parseDouble(txtTotal.getText()));
            fac.setFk_Encabezado(Integer.parseInt(txtFkEncabezado.getText()));
            fac.setFk_bebida(Integer.parseInt(txtFkBebida.getText()));
            fac.setFk_promocion(Integer.parseInt(txtFkPromocion.getText()));
            String mensaje = fbo.agregarFacturaDetalle(fac);
            limpiarCampos();
            listarFacturaDetalle();
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        if (camposVacios()) {
            JOptionPane.showMessageDialog(null, "Llene todos los campos");
        }else if (Integer.parseInt(txtIdFactura.getText()) > fbo.getMaxID()) {
            JOptionPane.showMessageDialog(null, "El registro con codigo "+txtIdFactura.getText()+" no existe");
        }else{
            Fac_Detalle fac = new Fac_Detalle();
            fac.setIdFac_Detalle(Integer.parseInt(txtIdFactura.getText()));
            fac.setCantidad(Integer.parseInt(txtCantidad.getText()));
            fac.setTotal(Double.parseDouble(txtTotal.getText()));
            fac.setFk_Encabezado(Integer.parseInt(txtFkEncabezado.getText()));
            fac.setFk_bebida(Integer.parseInt(txtFkBebida.getText()));
            fac.setFk_promocion(Integer.parseInt(txtFkPromocion.getText()));
            String mensaje = fbo.modificarFacturaDetalle(fac);
            limpiarCampos();
            listarFacturaDetalle();
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btmEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btmEliminarActionPerformed
            if (txtIdFactura.getText().isEmpty() || Integer.parseInt(txtIdFactura.getText()) > fbo.getMaxID()) {
            JOptionPane.showMessageDialog(null, "El registro con codigo "+txtIdFactura.getText()+" no existe");
             }else{
                String mensaje = fbo.eliminarFacturaDetalle(Integer.parseInt(txtIdFactura.getText()));
                limpiarCampos();
                listarFacturaDetalle();
             }
    }//GEN-LAST:event_btmEliminarActionPerformed

    private void tbFac_DetalleMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbFac_DetalleMouseClicked
        int seleccion = tbFac_Detalle.rowAtPoint(evt.getPoint());
        
        txtFkEncabezado.setText(tbFac_Detalle.getValueAt(seleccion, 0)+"");
        txtIdFactura.setText(tbFac_Detalle.getValueAt(seleccion, 1)+"");
        txtFkBebida.setText(tbFac_Detalle.getValueAt(seleccion, 2)+"");
        txtCantidad.setText(tbFac_Detalle.getValueAt(seleccion, 3)+"");
        txtFkPromocion.setText(tbFac_Detalle.getValueAt(seleccion, 4)+"");
        txtTotal.setText(tbFac_Detalle.getValueAt(seleccion, 5)+"");
        
    }//GEN-LAST:event_tbFac_DetalleMouseClicked

