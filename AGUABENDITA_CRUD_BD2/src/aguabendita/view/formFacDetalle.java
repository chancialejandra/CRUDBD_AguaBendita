    public void limpiarCampos(){
        txtCantidad.setText("");
        txtFkBebida.setText("");
        txtFkEncabezado.setText("");
        txtFkPromocion.setText("");
        txtIdFactura.setText("");
        txtTotal.setText("");
        idMax();
    }
    
    public boolean camposVacios(){
        if (txtCantidad.getText().isEmpty()
                ||txtFkBebida.getText().isEmpty()
                ||txtFkEncabezado.getText().isEmpty()
                ||txtFkPromocion.getText().isEmpty()
                ||txtIdFactura.getText().isEmpty()
                ||txtTotal.getText().isEmpty()) {
            return true;
        }else{
            return false;
        }
    }
    
